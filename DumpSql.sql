create or replace PROCEDURE set_appointment(d in date,l in number,d_no in number,c_id in varchar2,day in number) is
c number:=0;
flag number:=0;
BEGIN
for i in (select * from PERSON p1 where
CENTER_ID=c_id and
(select count(*) from VACCINATION_INFO
where PERSON_ID=p1.PERSON_ID
)=d_no-1
order by registration_date
)
LOOP
select count(*) into flag from APPOINTMENT where PERSON_ID=i.PERSON_ID;
if flag=d_no-1 then
flag:=0;
select count(*) into flag from APPOINTMENT where PERSON_ID=i.PERSON_ID and APPOINTMENT_DATE>d;
if (flag=0) then
if (d_no>1) then
select count(*) into flag from VACCINATION_INFO
where DOSE_NO=d_no-1 and d-VACCINATION_DATE>=day and PERSON_ID=i.PERSON_ID;
if (flag<>0) then
insert into APPOINTMENT values(sys_guid(),i.PERSON_ID,c_id,d);
c:=c+1;
end if;
else
insert into APPOINTMENT values(sys_guid(),i.PERSON_ID,c_id,d);
c:=c+1;
end if;
if (c=l) then
exit;
end if;
end if;
end if;
END LOOP;
end;
/

